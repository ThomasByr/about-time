import pytest

from about_time import HumanThroughput


@pytest.mark.parametrize(
    "count, duration, expected",
    [
        (1, 1.0, "1X/s"),
        (10, 1.0, "10X/s"),
        (1, 2.0, "30X/m"),
        (10, 2.0, "5X/s"),
        (11, 2.0, "5.5X/s"),
        (11, 1.981981981981982, "5.5X/s"),
        (10, 100.0, "6X/m"),
        (3, 100.0, "1.8X/m"),
        (8, 110.0, "4.4X/m"),
        (3, 1600.0, "6.8X/h"),
        (5432737542, 67587655435.0, "4.8X/m"),
        (543273754, 67587655435.0, "28.9X/h"),
        (543273754271, 67587655435.0, "8X/s"),
        (1, 0.99, "1X/s"),
        (1, 1.0001, "1X/s"),
        (123, 1165263.0, "9.12X/d"),
        (1, 3599.0, "1X/h"),
        (1, 3601.0, "23.99X/d"),
        (1, 3608.0, "23.95X/d"),
        (2, 80000.0, "2.16X/d"),
    ],
)
def test_throughput_human(count, duration, expected):
    assert HumanThroughput(count / duration, "X") == expected
    assert HumanThroughput(count / duration, "!").unit("X") == expected


