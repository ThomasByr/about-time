import pytest

from about_time import HumanDuration


@pytest.mark.parametrize(
    "duration, expected",
    [
        (0.00000000123, "1.2ns"),
        (0.00000000185, "1.9ns"),
        (0.000001, "1µs"),
        (0.000000999996, "1µs"),
        (0.00001, "10µs"),
        (0.0000156, "15.6µs"),
        (0.01, "10ms"),
        (0.0141233333333, "14.1ms"),
        (0.000999999, "1ms"),
        (0.0199999, "20ms"),
        (0.1099999, "110ms"),
        (0.160594034, "160.6ms"),
        (0.80153, "801.5ms"),
        (3.434999, "3.43s"),
        (3.435999, "3.44s"),
        (59.9, "59.9s"),
        (59.99, "59.99s"),
        (59.999, "1:00"),
        (60.0, "1:00"),
        (60.99, "1:01"),
        (68.0, "1:08"),
        (68.09, "1:08.1"),
        (125.825, "2:05.8"),
        (3888.395, "1:04:48"),
    ],
)
def test_duration_human(duration, expected):
    assert HumanDuration(duration) == expected


